public class StringExercise {
    public static void main(String[] args) {
        String noEducation = "We don't need no education\n" +
                "We don't need no thought control\n";
        System.out.println(noEducation);

        String checkItOut = "Check \"this\" out!, \"s inside of \"s!\n";
        System.out.println(checkItOut);

        String mainDrive = "In windows, the main drive is usually C:\\\n";
        System.out.println(mainDrive);

        String backslashes = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!\n";
        System.out.println(backslashes);
    }
}

